/*
 Not ! logic operator -> 
                    true - false, false - true
*/

let temp = 12;
let sunny = false;

if (!(temp > 0)) {
    console.log("it's cold outside!")
} else {
    console.log("it's warm outside!")
}

if (!sunny) {
    console.log("it's cold outside!")
} else {
    console.log("it's warm outside!")
}




